export type TranslationKey = keyof typeof translations.en;

export const getTranslation = (language: 'en' | 'af', key: string, defaultValue: string = ''): string => {
  try {
    const keys = key.split('.');
    let current: any = translations[language];
    for (const k of keys) {
      current = current[k];
    }
    return current || defaultValue;
  } catch {
    return defaultValue;
  }
};
